#ifndef REDSHIFT_HAL_CPU_TSS_H
#define REDSHIFT_HAL_CPU_TSS_H

#define TSS_SELECTOR 0x28

#ifndef __ASM_SOURCE__
# include <redshift/kernel.hpp>

namespace redshift { namespace hal { namespace cpu_detail {
    struct tss {
        uint32_t reserved;
        uint32_t esp0;
        uint32_t ss0;
        uint32_t esp1;
        uint32_t ss1;
        uint32_t esp2;
        uint32_t ss2;
        uint32_t cr3;
        uint32_t eip;
        uint32_t eflags;
        uint32_t eax;
        uint32_t ecx;
        uint32_t edx;
        uint32_t ebx;
        uint32_t esp;
        uint32_t ebp;
        uint32_t esi;
        uint32_t edi;
        uint32_t es;
        uint32_t cs;
        uint32_t ss;
        uint32_t ds;
        uint32_t fs;
        uint32_t gs;
        uint32_t ldt;
        uint16_t trap;
        uint16_t iobt;

        static tss& init();

        static tss& instance();

        static void load();

        static uintptr_t base();

        static size_t size();
    protected:
        tss() = default;
    private:
        static tss m_instance;
    } __packed;
}}}

#endif // ! __ASM_SOURCE__

#endif // ! REDSHIFT_HAL_CPU_TSS_H
